def get_betas(schedule_type, b_start, b_end, time_num):
    if schedule_type == 'linear':
        betas = np.linspace(b_start, b_end, time_num)
    elif schedule_type == 'warm0.1':

        betas = b_end * np.ones(time_num, dtype=np.float64)
        warmup_time = int(time_num * 0.1)
        betas[:warmup_time] = np.linspace(b_start, b_end, warmup_time, dtype=np.float64)
    elif schedule_type == 'warm0.2':

        betas = b_end * np.ones(time_num, dtype=np.float64)
        warmup_time = int(time_num * 0.2)
        betas[:warmup_time] = np.linspace(b_start, b_end, warmup_time, dtype=np.float64)
    elif schedule_type == 'warm0.5':

        betas = b_end * np.ones(time_num, dtype=np.float64)
        warmup_time = int(time_num * 0.5)
        betas[:warmup_time] = np.linspace(b_start, b_end, warmup_time, dtype=np.float64)
    else:
        raise NotImplementedError(schedule_type)
    return betas


def main():
    dataset = ShapeNetCoreV2PC15KDataset(
        root_path="data/ShapeNetCore.v2.PC15k",
        categories=["chair"],
        train_batch_size=16,
        val_batch_size=16,
        num_workers=8,
    )

    dataloader = dataset.train_dataloader()

    betas = get_betas("linear", 0.0001, 0.02, 1000)
    diffusion = GaussianDiffusion(betas, "mse", "eps", "fixedsmall")

    model = PVCNN()
    model.cuda()

    tmp = model.configure_optimizers()
    optimizer = tmp["optimizer"]
    lr_scheduler = tmp["lr_scheduler"]

    noise_scheduler = model.noise_scheduler

    for epoch in range(100):
        print("epoch", epoch)
        lr_scheduler.step(epoch)

        model.train()

        for batch in tqdm(dataloader):
            batch.pcs = batch.pcs.cuda()
            loss = model.training_step(batch, 0)

            optimizer.zero_grad()
            loss.backward()
            optimizer.step()

        if (epoch + 1) % 10 == 0:
            print("vis", epoch)

            model.eval()

            def _denoise(data, t):
                B, D,N= data.shape
                assert data.dtype == torch.float
                assert t.shape == torch.Size([B]) and t.dtype == torch.int64

                points = PointTensor(
                    coords=data[:, :3, :].permute(0, 2, 1),
                    features=data,
                    is_channel_last=False,
                    timesteps=t,
                )

                out = model.model(points)
                # out = self.model(data, t)

                assert out.shape == torch.Size([B, D, N])
                return out

            with torch.no_grad():
                x_gen_eval = diffusion.p_sample_loop(
                    _denoise,
                    shape=(25, 3, 2048),
                    device=model.device,
                    noise_fn=torch.randn,
                    clip_denoised=False,
                )

                visualize_pointcloud_batch(
                    f"output/test3/epoch_{epoch:03d}.png",
                    x_gen_eval.transpose(1, 2), None, None, None
                )


if __name__ == "__main__":
    main()
